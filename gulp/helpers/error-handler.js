import notify from "gulp-notify";

// #####################################################################################################################

/**
 * Simple error handler
 * @param error
 */
function errorHandler(error) {
    notify.onError({
        title: error.code,
        message: error.message,
        sound: "Beep"
    })(error);
}

// #####################################################################################################################

export default errorHandler;

